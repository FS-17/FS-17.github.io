import Link from "next/link";

export default function ProjectCard({ project, lang = "en" }) {
  const isAr = lang === "ar";
  const basePath = isAr ? "/ar/projects" : "/projects";

  // Logic from index.html script
  let mediaHTML = null;
  if (project.media && project.media.length > 0) {
    const firstMedia = project.media[0];
    if (firstMedia.type === "logo") {
      mediaHTML = (
        <div className="relative mb-6 flex justify-center">
          <div className="absolute inset-0 bg-gradient-to-r from-blue-500/20 to-cyan-500/20 rounded-xl blur-xl opacity-0 group-hover:opacity-100 transition-opacity duration-500" />
          <img
            src={firstMedia.url}
            alt={firstMedia.alt || project.title}
            className="relative h-20 object-contain transition-transform duration-500 group-hover:scale-110"
          />
        </div>
      );
    }
  }

  const primaryLink = `${basePath}/${project.slug}`;

  return (
    <div className="project-card group relative p-8 rounded-2xl glass-card border border-white/5 flex flex-col h-full overflow-hidden">
      {/* Hover gradient background */}
      <div className="absolute inset-0 bg-gradient-to-br from-blue-500/5 via-transparent to-cyan-500/5 opacity-0 group-hover:opacity-100 transition-opacity duration-500" />

      {/* Shine effect on hover */}
      <div className="absolute inset-0 opacity-0 group-hover:opacity-100 transition-opacity duration-500">
        <div className="absolute top-0 -left-full w-full h-full bg-gradient-to-r from-transparent via-white/5 to-transparent group-hover:left-full transition-all duration-1000" />
      </div>

      <Link
        href={primaryLink}
        className="absolute inset-0 z-10"
        aria-label={`View details of ${project.title}`}
      />

      <div className="flex-grow relative z-2">
        {mediaHTML}

        <h3 className="text-xl font-bold mb-3 text-white group-hover:text-transparent group-hover:bg-gradient-to-r group-hover:from-blue-400 group-hover:to-cyan-400 group-hover:bg-clip-text transition-all duration-300">
          {project.title}
        </h3>

        <p className="text-gray-400 mb-6 leading-relaxed line-clamp-3">
          {project.shortDescription}
        </p>

        <div className="flex flex-wrap gap-2 mb-4">
          {project.tags &&
            project.tags.slice(0, 4).map((tag, index) => (
              <span
                key={index}
                className="tag px-3 py-1.5 text-xs font-medium rounded-lg bg-white/5 border border-white/10 text-gray-300"
              >
                {tag}
              </span>
            ))}
          {project.tags && project.tags.length > 4 && (
            <span className="px-3 py-1.5 text-xs font-medium rounded-lg bg-blue-500/10 border border-blue-500/20 text-blue-400">
              +{project.tags.length - 4}
            </span>
          )}
        </div>
      </div>

      <div className="flex gap-3 mt-auto pt-6 border-t border-white/5 relative z-20 pointer-events-none">
        {project.links?.repo && (
          <a
            href={project.links.repo}
            className="flex items-center gap-2 px-4 py-2.5 rounded-xl bg-white/5 hover:bg-white/10 border border-white/10 hover:border-blue-500/30 text-gray-300 hover:text-white transition-all duration-300 text-sm font-medium pointer-events-auto"
            onClick={(e) => e.stopPropagation()}
            target="_blank"
            rel="noopener noreferrer"
          >
            <svg className="w-4 h-4" fill="currentColor" viewBox="0 0 24 24">
              <path d="M12 0c-6.626 0-12 5.373-12 12 0 5.302 3.438 9.8 8.207 11.387.599.111.793-.261.793-.577v-2.234c-3.338.726-4.033-1.416-4.033-1.416-.546-1.387-1.333-1.756-1.333-1.756-1.089-.745.083-.729.083-.729 1.205.084 1.839 1.237 1.839 1.237 1.07 1.834 2.807 1.304 3.492.997.107-.775.418-1.305.762-1.604-2.665-.305-5.467-1.334-5.467-5.931 0-1.311.469-2.381 1.236-3.221-.124-.303-.535-1.524.117-3.176 0 0 1.008-.322 3.301 1.23.957-.266 1.983-.399 3.003-.404 1.02.005 2.047.138 3.006.404 2.291-1.552 3.297-1.23 3.297-1.23.653 1.653.242 2.874.118 3.176.77.84 1.235 1.911 1.235 3.221 0 4.609-2.807 5.624-5.479 5.921.43.372.823 1.102.823 2.222v3.293c0 .319.192.694.801.576 4.765-1.589 8.199-6.086 8.199-11.386 0-6.627-5.373-12-12-12z" />
            </svg>
            <span>{isAr ? "المستودع" : "Code"}</span>
          </a>
        )}
        {project.links?.demo && (
          <a
            href={project.links.demo}
            className="flex items-center gap-2 px-4 py-2.5 rounded-xl bg-gradient-to-r from-blue-500/10 to-cyan-500/10 hover:from-blue-500/20 hover:to-cyan-500/20 border border-blue-500/20 hover:border-blue-500/40 text-blue-400 hover:text-blue-300 transition-all duration-300 text-sm font-medium pointer-events-auto"
            onClick={(e) => e.stopPropagation()}
            target="_blank"
            rel="noopener noreferrer"
          >
            <svg
              className="w-4 h-4"
              fill="none"
              stroke="currentColor"
              viewBox="0 0 24 24"
            >
              <path
                strokeLinecap="round"
                strokeLinejoin="round"
                strokeWidth={2}
                d="M10 6H6a2 2 0 00-2 2v10a2 2 0 002 2h10a2 2 0 002-2v-4M14 4h6m0 0v6m0-6L10 14"
              />
            </svg>
            <span>{isAr ? "معاينة" : "Demo"}</span>
          </a>
        )}

        {/* View project arrow */}
        <div className="ml-auto flex items-center text-gray-500 group-hover:text-blue-400 transition-colors duration-300">
          <svg
            className="w-5 h-5 transform group-hover:translate-x-1 transition-transform duration-300"
            fill="none"
            stroke="currentColor"
            viewBox="0 0 24 24"
          >
            <path
              strokeLinecap="round"
              strokeLinejoin="round"
              strokeWidth={2}
              d="M17 8l4 4m0 0l-4 4m4-4H3"
            />
          </svg>
        </div>
      </div>
    </div>
  );
}
